# 0-49-F
# 50-59-D
# 60-69-C
# 70-79-B
# 80-89-A
# 90-100-A+

Score =input ("Whats your Score?")
int_Score=int(Score)
if int_Score <=49:
    print("F")
elif int_Score <=59:
    print("D")
elif int_Score<=69:
    print("C")
elif int_Score<=79:
    print("B")
elif int_Score<=89:
    print("A")
elif int_Score<=100:
    print("A+")
    